    "    df = discovery.withColumn(\"row_num\", F.row_number().over(window_latest)) \\\n",
    "                  .filter(F.col(\"row_num\") == 1) \\\n",
    "                  .drop(\"row_num\") \\\n",
    "                  .select(\n",
    "                      \"ticker\",\n",
    "                      \"sub_industry\",\n",
    "                      \"log_market_cap\",\n",
    "                      \"ebitda_margin\",\n",
    "                      \"roic\",\n",
    "                      F.col(\"fetched_ts\").alias(\"profile_ts\")\n",
    "                  )\n",
    "\n",
    "    df = df.withColumn(\n",
    "        \"market_cap_tier\",\n",
    "        F.when(F.col(\"log_market_cap\") >= 25.3, \"mega_cap\")\n",
    "         .when(F.col(\"log_market_cap\") >= 23.0, \"large_cap\")\n",
    "         .when(F.col(\"log_market_cap\") >= 20.7, \"mid_cap\")\n",
    "         .otherwise(\"small_cap\")\n",
    "    )\n",
    "\n",
    "    return df"
